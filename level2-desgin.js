function preload() {
  this.load.image("floor", "assets/screen.png");
  this.load.image("wall", "assets/tileset.png");
  this.load.image("player", "assets/character.png");
}

function create() {
  const tileSize = 32;
  this.walls = this.physics.add.staticGroup();

  for (let row = 0; row < mapData.length; row++) {
    for (let col = 0; col < mapData[row].length; col++) {
      let tileType = mapData[row][col];
      let x = col * tileSize;
      let y = row * tileSize;
      if (tileType === 0) {
        this.add.image(x, y, "floor").setOrigin(0);
      } else if (tileType === 1) {
        const wall = this.walls.create(x, y, "wall").setOrigin(0).refreshBody();
      }
    }
  }
  this.player = this.physics.add.sprite(50, 550, "player").setScale(0.4);
}
